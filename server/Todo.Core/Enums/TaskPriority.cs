namespace Todo.Core.Enums;

/// <summary>
///     Represents the priority of a task.
/// </summary>
public enum TaskPriority
{
    /// <summary>
    ///     Represents the highest priority.
    /// </summary>
    Urgent = 0,

    /// <summary>
    ///     Represents a high priority.
    /// </summary>
    High = 1,

    /// <summary>
    ///     Represents a medium priority.
    /// </summary>
    Medium = 2,

    /// <summary>
    ///     Represents a low priority.
    /// </summary>
    Low = 3
}